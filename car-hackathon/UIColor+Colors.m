#import "UIColor+Colors.h"

@implementation UIColor (Colors)

+ (UIColor *)customOrange {
    return [UIColor colorWithRed:0.816 green:0.494 blue:0.137 alpha:1];
}

+(UIColor *)customGray {
    return [UIColor colorWithRed:35/256 green:35/256 blue:35/256 alpha:1];
}

@end
